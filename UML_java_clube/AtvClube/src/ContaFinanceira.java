public class ContaFinanceira {
    double chaveAcesso;
    float saldo;

    // get e set: chave, saldo
    double getChaveAcesso(){
        return chaveAcesso;
    }
    void setChaveAcesso(double chaveAcesso){
        this.chaveAcesso = chaveAcesso;
    }
    double getSaldo(){
        return saldo;
    }
    void setSaldo(float saldo){
        this.saldo = saldo;
    }
}
